integers = [4, 6, 3, 8, 6, 1]
#La lista integers recibe los numeros
numero_mayor = max(integers)
#La variable "numero_mayor" recibe el numero mayor que hay en la lista
numero_menor = min(integers)
#la variable "numero_menor" recibe el numero menor que hay en la lista
diferencia = numero_mayor - numero_menor
#la variable diferencia recibe la resta entre el numero mayor y el menor para recibir la diferencia entre si
print("El numero mayor de la lista es:", numero_mayor)
#este print imprime el numero mayor
print("El numero menor de la lista es:", numero_menor)
#este print imprime el numero menor
print("la diferencia entre el mayor de los numeros es de:", diferencia)
